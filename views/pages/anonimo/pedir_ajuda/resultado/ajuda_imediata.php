        <p>Precisa de Ajuda Imediata? Saiba o Que Fazer Agora!

        Se você está em uma situação de violência ou se sente em perigo, é fundamental agir rapidamente para garantir sua segurança. Aqui estão os passos que você deve seguir imediatamente:

        Busque um Local Seguro: Se possível, saia do ambiente de perigo e vá para um local onde você se sinta seguro(a), como a casa de um amigo, parente ou um local público.
        
        Peça Ajuda Imediata:
        Ligue para a polícia no número 190 se estiver em uma situação de emergência ou se precisar de intervenção imediata.
        Se for um caso de violência doméstica, você também pode ligar para o Disque 180 para apoio especializado e orientações.

        Procure Atendimento Médico: Se sofreu algum tipo de lesão física, dirija-se ao hospital ou a um pronto-socorro para receber os cuidados necessários.

        Busque Suporte Legal:
        Delegacia de Polícia: Você pode registrar um boletim de ocorrência na delegacia mais próxima ou, em casos de violência doméstica, procurar uma Delegacia Especializada de Atendimento à Mulher (DEAM).
        Medidas Protetivas: Considere solicitar medidas protetivas legais que possam manter o agressor afastado.

        Entre em Contato com um Profissional de Saúde Mental: Se precisar de apoio emocional, procure um psicólogo ou um serviço de apoio emocional como o CVV (Centro de Valorização da Vida) no número 188.

        Recursos Específicos:
        Centros de Referência da Mulher: Oferece atendimento psicológico, jurídico e de assistência social para mulheres em situação de violência.
        Defensoria Pública: Disponibiliza apoio legal gratuito, incluindo orientação sobre como proceder judicialmente.

        Importante: Guarde todos os registros e evidências (como mensagens, e-mails ou fotos de lesões) que possam ser úteis para uma possível ação legal.

        Lembre-se: Você não está sozinho(a). Existem recursos e pessoas prontas para ajudar você a se proteger e garantir seus direitos. A segurança é a prioridade agora. Aja rapidamente e busque o apoio necessário!
