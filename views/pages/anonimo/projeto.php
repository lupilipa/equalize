                <h2>Base Teórica</h2>
                <p>O projeto Equalize é fundamentado em diversas teorias que abordam a violência de gênero como um fenômeno complexo. A teoria feminista aponta a violência como uma ferramenta para manter a desigualdade de gênero, enquanto Judith Butler destaca o papel da sociedade na construção de normas de gênero que perpetuam comportamentos violentos. A teoria da interseccionalidade de Kimberlé Crenshaw enfatiza como múltiplas formas de opressão se intersectam, criando experiências únicas de violência. Além disso, a teoria do ciclo da violência de Lenore Walker e a teoria da aprendizagem social de Albert Bandura destacam a necessidade de intervenções educativas para quebrar padrões de violência e ensinar comportamentos saudáveis. A abordagem do projeto também se apoia na teoria da difusão de inovações de Everett Rogers, utilizando a tecnologia para alcançar e impactar positivamente a comunidade escolar. Combinando esses fundamentos teóricos, o Equalize visa transformar o ambiente escolar em um espaço seguro, inclusivo e informativo, promovendo mudanças culturais necessárias para erradicar a violência de gênero.</p>
