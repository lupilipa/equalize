                <h2>Justificativa</h2>
                <p>A violência de gênero é uma problemática persistente, enraizada em muitas sociedades, incluindo o Brasil, e afeta significativamente o <strong class="projeto">ambiente escolar</strong>. Esse tipo de violência se manifesta através de agressões físicas, verbais, assédio e discriminação, impactando negativamente o desenvolvimento emocional, psicológico e acadêmico das vítimas. O projeto Equalize surgiu da necessidade urgente de uma <strong class="projeto">ferramenta eficaz</strong> para lidar com a violência de gênero nas escolas, oferecendo conscientização e suporte de maneira prática e acessível. A plataforma visa <strong class="projeto">criar um ambiente seguro e acolhedor para todos</strong>, permitindo que vítimas relatem incidentes de forma anônima, incentivando a busca por ajuda sem medo de represálias ou estigmatização. Além disso, o Equalize educa a comunidade escolar, incluindo alunos, professores e funcionários, <strong class="projeto">promovendo uma cultura de respeito</strong> e igualdade que ajuda a prevenir a perpetuação do ciclo de violência.</p>
