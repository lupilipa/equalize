        <h1>Contatos</h1>
        <p>Contatos

        Se você precisa de ajuda ou quer relatar um incidente, aqui estão as principais formas de contato disponíveis tanto na escola quanto em âmbito geral. Lembre-se: você tem o direito de ser ouvido(a) e protegido(a).

        Contatos da Escola:
        Coordenação/Orientação Educacional: Disponível para orientações, denúncias e suporte. Procure a sala da coordenação.
        Contatos Gerais:
        Disque 180 (Central de Atendimento à Mulher): Para orientação e denúncia de violência contra a mulher, disponível 24 horas por dia, todos os dias.
        Disque 100 (Disque Direitos Humanos): Para denúncias de violações de direitos humanos, incluindo violência contra crianças, adolescentes, idosos e pessoas com deficiência.
        Polícia Militar: Ligue 190 em casos de emergência.
        Centro de Valorização da Vida (CVV): Ligue 188 para apoio emocional gratuito e sigiloso, disponível 24 horas por dia.

        Citação Legal:
        Lei Maria da Penha (Lei nº 11.340/2006): “A violência doméstica e familiar contra a mulher constitui uma das formas de violação dos direitos humanos.”
        Esta lei protege mulheres de todas as idades contra violência física, psicológica, sexual, patrimonial e moral. Ela prevê medidas protetivas urgentes para afastar o agressor e garantir a segurança da vítima.

        Estatuto da Criança e do Adolescente (ECA, Lei nº 8.069/1990): “É dever de todos prevenir a ocorrência de ameaça ou violação dos direitos da criança e do adolescente.”
        O ECA assegura proteção integral aos direitos das crianças e adolescentes, incluindo a proteção contra qualquer forma de violência ou abuso.

        Se você está em situação de violência ou conhece alguém que está, não hesite em buscar ajuda. A lei está ao seu lado, e existem recursos para proteger e apoiar você.
